export type SebPart = Part & {
	seb: string[];
};

function findSebs(text: string): SebPart[] {
	const result: SebPart[] = [];

	// Return early if we don't have any nicknames to find
	if (sebs.length === 0) {
		return result;
	}

	const sebsTrimmed = sebs.map(function (seb) {
		return seb[2];
	});

	let match: RegExpExecArray | null;

	while ((match = sebRegExp.exec(text))) {
		const matchIndex = sebsTrimmed.indexOf(match[1]);

		if (matchIndex > -1) {
			const seb = sebs[matchIndex];

			if (seb.length > 0) {
				result.push({
					start: match.index,
					end: match.index + seb[1].length,
					seb: seb,
				});
			}
		}
	}

	return result;
}

